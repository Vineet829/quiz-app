let card_list = document.querySelectorAll(".card");
let question_box = document.querySelector(".question-container");
let random = document.querySelector(".random-btn")

let score = 0

let isSelected = false
let answer = ""
let index = 0
random.addEventListener("click", (e) => {
  e.stopPropagation(); 
  main.style.display = "none";
  let section_index = Math.floor(Math.random() * 8)
    question_box.style.display = "flex"; 
  document.body.style.backgroundImage = "none"
           questionGenerator(section_index)
})

card_list.forEach((item) => {
    item.addEventListener("click", (e) => {
      e.stopPropagation(); 
      main.style.display = "none";
        let section_index = item.getAttribute("data-section");
          question_box.style.display = "flex"; 
        document.body.style.backgroundImage = "none"
                 questionGenerator(section_index)
       
           
    });
});

function questionGenerator(section_index) {
  if (index >= data.section[section_index].length) {
    
    question_box.innerHTML = `
    <div class="question-container" style="display: flex;">
            <img src="./images/quiz_end.jpg" alt="quizEnd">
            <div class="scoreElement>
                <h1 class=" quizend-h1"="">QUIZ Completed
                <p>Your Final Score is : ${score}/10</p>
                <button id="home-btn">Go To Home</button>
            </div>
            </div>
    `;
    document.querySelector("#home-btn").addEventListener("click", () => {
      question_box.style.display = "none"
      main.style.display = "block"
      document.body.style.backgroundImage = "url(./images/bg.jpg)"
    })
    index = 0
    score = 0
    return;
} 
  
  question_box.innerHTML = `
    <div class="questions-content">${data.section[section_index][index].question}</div>
    <div class="options">
        <ul>
            <li>${data.section[section_index][index].options[0]}</li>
            <li>${data.section[section_index][index].options[1]}</li>
            <li>${data.section[section_index][index].options[2]}</li>
            <li>${data.section[section_index][index].options[3]}</li>
            
        </ul>
        <p class="right" style="display: none;">Correct Answer!</p>
        <p class="wrong" style="display: none;"></p>
        <button class="next-btn" style="display: none">Next</button>
    </div>
`;
document.querySelector(".options").addEventListener("click", (e) => {
    
    
    
    
     if(!isSelected && e.target.tagName === "LI" ){
      answer= e.target.textContent  
      isSelected = true
        e.target.setAttribute("class", "selected")
        if(answer === data.section[section_index][index].correct_answer){
            
            document.querySelector(".right").style.display = "block"
            score++
         

        }
        else{
           let element =  document.querySelector(".wrong")
            element.innerHTML = `
           Wrong Answer! ${data.section[section_index][index].correct_answer}`
           element.style.display = "block"
           
        }
            document.querySelector(".next-btn").style.display = "block"
     }

     })          

     document.querySelector(".next-btn").addEventListener("click", (e) => {
      index++
      isSelected = false
      questionGenerator(section_index)
    })
    }
